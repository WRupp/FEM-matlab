function B = Btriquad(e1,e2,X,Y)

	N(1,1) = 4*e1+4*e2-1;
	N(2,1) = 4*e1 -1 ;
	N(3,1) = 0 ;
	N(4,1) = 4*(1-2*e1-2*e2);
	N(5,1) = 4*e2;
	N(6,1) = -4*e2;

	N(1,2) = 4*e1+4*e2-3;
	N(2,2) = 0;
	N(3,2) = 4*e2-1;
	N(4,2) = -4*e1;
	N(5,2) = 4*e1;
	N(6,2) = 4*(1-e1-2*e2);

	% Definicao da Jacobiana

	J(1,1) = N(:,1)'*X; 
	J(1,2) = N(:,2)'*X;
	J(2,1) = N(:,1)'*Y;
	J(2,2) = N(:,2)'*Y;

	%

	Bt1 = [ N(1,1) 0       N(2,1) 0      N(3,1) 0      N(4,1) 0      N(5,1) 0      N(6,1) 0 ;
			0      N(1,1)  0      N(2,1) 0      N(3,1) 0      N(4,1) 0      N(5,1) 0      N(6,1);
			N(1,1) N(1,1)  N(2,1) N(2,1) N(3,1) N(3,1) N(4,1) N(4,1) N(5,1) N(5,1) N(6,1) N(6,1)];

	Bt1(1,:) = 	1/J(1,1)*	Bt1(1,:);
	Bt1(2,:) = 	1/J(2,1)*	Bt1(2,:);

	Bt1(3,[1,3,5,7,9,11]) = 1/J(1,1).* Bt1(3,[1,3,5,7,9,11]);
	Bt1(3,[2,4,5,8,10,12]) = 1/J(1,1).* Bt1(3,[2,4,5,8,10,12]);

	Bt2 = [ N(1,2) 0       N(2,2) 0      N(3,2) 0      N(4,2) 0      N(5,2) 0      N(6,2) 0 ;
			0      N(1,2)  0      N(2,2) 0      N(3,2) 0      N(4,2) 0      N(5,2) 0      N(6,2);
			N(1,2) N(1,2)  N(2,2) N(2,2) N(3,2) N(3,2) N(4,2) N(4,2) N(5,2) N(5,2) N(6,2) N(6,2)];

	Bt2(1,:) = 	1/J(1,2)*	Bt2(1,:);
	Bt2(2,:) = 	1/J(2,2)*	Bt2(2,:);

	Bt2(3,[1,3,5,7,9,11]) = 1/J(1,1).* Bt2(3,[1,3,5,7,9,11]);
	Bt2(3,[2,4,5,8,10,12]) = 1/J(1,1).* Bt2(3,[2,4,5,8,10,12]);

	B = Bt1 + Bt2;


end